Radius=float(input('Enter the radius of the cylinder:'))
Height=float(input('Enter the height of the cylinder'))
pi=3.14
Side_area=(2*pi*Radius*Height)
cylinder_volume=(pi*Radius*Radius*Height)
Total_area=(Side_area)+(2*pi*Radius*Radius)
print('area of the cylinder=',Side_area )
print('cylinder volume=',cylinder_volume )
print('cylinder area=',Total_area)